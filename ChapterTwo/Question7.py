# 2.7 (Multiples) Use if statements to determine whether 1024 is a multiple
# of 4 and whether 2 is a multiple of 10. (Hint: Use the remainder operator.)

first_number = 1024
if first_number % 4 == 0:
    print("1024 is a multiple of 4")
else:
    print("1024 is not a multiple of 4")

second_number = 2
if second_number % 10 == 0:
    print("2 is a multiple of 10")
else:
    print("2 is not a multiple of 10")
